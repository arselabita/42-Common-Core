char	*ft_strchr(const char *s, int c)
{
	int	i;
	char	ch = (char)c;

	i = 0;
	if(ch == '\0')
		return (char *)&s[i];
	while (s[i])
	{
		if(s[i] == ch) return (char *)&s[i];
		i++;
	}
	return (0);
}
